-- MOSTRAR EL NOMBRE Y APELLIDO DE LOS ACTORES CUYO RATING SEA MAYOR A 7.5
SELECT CONCAT(first_name, ' ', last_name) "Nombre Completo", rating "Rating" FROM actors WHERE rating > 7.5;

-- MOSTRAR EL TITULO DE LAS PELÍCULAS, EL RATING Y LOS PREMIOS DE LAS PELÍCULAS
-- CON UN RATING MAYOR A 7.5 Y CON MÁS DE DOS PREMIOS
SELECT title "Titulo", rating "Rating", awards "Premios" FROM movies
WHERE rating > 7.5 AND awards > 2;

-- MOSTRAR EL TÍTULO DE LAS PELÍCULAS Y EL RATING ORDENADAS POR RATING EN FORMA ASCENDENTE
SELECT title "Titulo", rating "Rating" FROM movies
ORDER BY rating ASC;

-- MOSTRAR LOS TÍTULOS DE LAS PRIMERAS TRES PELÍCULAS EN LA BASE DE DATOS
SELECT title "Titulo" FROM movies LIMIT 3;

-- MOSTRAR EL TOP 5 DE LAS PELÍCULAS CON MAYOR RATING
SELECT title "Titulo", rating "Rating" FROM MOVIES ORDER BY rating DESC LIMIT 3;

-- LISTAR LOS PRIMEROS 10 ACTORES
