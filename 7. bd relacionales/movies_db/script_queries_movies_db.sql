-- MOSTRAR EL TÍTULO Y RATING DE TODAS LAS PELÍCULAS CUYO TÍTULO SEA DE TOY STORY
SELECT title "Titulo" FROM movies WHERE title LIKE "Toy Story%";

-- MOSTRAR TODOS LOS ACTORES CUYOS NOMBRES EMPIEZAN CON SAM
SELECT CONCAT(first_name, ' ', last_name) "Nombre Completo" FROM actors WHERE first_name LIKE "Sam%";

-- MOSTRAR EL TÍTULO DE LAS PELICULAS QUE SALIERON ENTRE EL 2004 Y 2008
SELECT title "Titulo" FROM movies WHERE YEAR(release_date) BETWEEN 2004 AND 2008;

-- TRAER EL TÍTULO DE LAS PELÍCULAS CON EL RATING MAYOR A 3, CON MÁS DE 1 PREMIO Y CON FECHA DE LANZAMIENTO ENTRE
-- EL AÑO 1988 AL 2009. ORDENAR LOS RESULTADOS POR RATING
SELECT title "Titulo" FROM movies WHERE YEAR(release_date) BETWEEN 1988 AND 2009
